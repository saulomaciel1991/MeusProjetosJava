    public void preencherTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Posto", "Nome", "PREC-CP", "Setor", "Sexo", "Hora inicial", "Hora Limite"};
        con.conexao();
        con.executaSql(Sql);

        try {
            con.rs.first();
            do {
                dados.add(new Object[]{con.rs.getString("posto_usu"), con.rs.getString("nome_usu"), con.rs.getString("preccp"), con.rs.getString("setor_usu"), con.rs.getString("sexo_usu"), con.rs.getString("horalimite_usu"), con.rs.getString("horalimite2_usu")});

            } while (con.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayList!!" + ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTableUsuarios.setModel(modelo);
        jTableUsuarios.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableUsuarios.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableUsuarios.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(2).setPreferredWidth(92);
        jTableUsuarios.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableUsuarios.getColumnModel().getColumn(3).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(4).setPreferredWidth(80);
        jTableUsuarios.getColumnModel().getColumn(4).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(5).setPreferredWidth(80);
        jTableUsuarios.getColumnModel().getColumn(5).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(6).setPreferredWidth(80);
        jTableUsuarios.getColumnModel().getColumn(6).setResizable(false);
        jTableUsuarios.getTableHeader().setReorderingAllowed(false);
        jTableUsuarios.setAutoResizeMode(jTableUsuarios.AUTO_RESIZE_OFF);
        jTableUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

